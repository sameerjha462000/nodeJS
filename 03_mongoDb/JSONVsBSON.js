/*
    What is JSON??
    1. JavaScript Object Notation, commonly known as JSON was defined as part of the JavaScript langauge in the early 2000s by JavaScript Creator
       Dougls Crockford, though it was not until 2013 that the format was officially specified.
    2. JavaScript objects are simple associative containers, wherein a string key is mapped to a value (which can be a string, number, boolean, function
       or even another object.)
    3. JSON values can be string, Boolean, Number, Array.

    But JSON has its limitations.
    1. JSON is a text-based format, and text parsing is very slow.
    2. JSON's readable format is far from space-efficient, another database concern.
    3. JSON only supports a limited number of basic data types.

    So, In order to make MongoDB JSON-first, but still high performance BSON was invencted.

    What is BSON??
    1. BSON simply stands for "Binary JSON" and that's exactly what it was invented to be.
    2. BSON values can be String, Boolean, Number(Integer, Float, Long, Decimal128), Array, Date, Raw Binary

*/